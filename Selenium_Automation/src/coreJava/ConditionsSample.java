package coreJava;

public class ConditionsSample {
	public static void main(String[] args) {
		int a = 90;
		int b = 90;
		
		//conditions
		if (a>b) {
			System.out.println("a is large value");
		} else if(b>a) {
			System.out.println("b is large value");
		}else {
			System.out.println("a and b both are equal");
		}
		
	}

}
